#include "3-calc.h"

/**
 * op_add - gives the sum of a and b
 * @a: first number
 * @b: second number
 * Return: returns the result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - gives the difference a and b
 * @a: 1st number
 * @b: 2nd number
 * Return: the result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - gives the product of a and b
 * @a: 1st number
 * @b: 2nd number
 * Return: the result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - gives the division of a and b
 * @a: 1st number
 * @b: 2nd number
 * Return: the result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - gives the remainder of the division
 * @a: 1st number
 * @b: 2nd number
 * Return: the result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
